public class Car {
    private String name;
   private int speed;
    private int fuelInTank;

    public void setName(String name) {
        this.name = name;
    }
    public String getName(){
        return name;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getSpeed() {
        return speed;
    }

    public void setFuelInTank(int fuelInTank) {
        this.fuelInTank = fuelInTank;
    }

    public int getFuelInTank() {
        return fuelInTank;
    }
}


